public class MotorVehicle {

    public String mark;
    public String model;
    public String color;
    public double length;
    public double width;
    public double height;
    public Engine engine;


    public MotorVehicle(String mark, String model, String color, double length, double width, double height, Engine engine) {
        this.mark = mark;
        this.model = model;
        this.color = color;
        this.length = length;
        this.width = width;
        this.height = height;
        this.engine = engine;
    }


}
